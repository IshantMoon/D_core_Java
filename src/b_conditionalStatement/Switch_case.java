package b_conditionalStatement;

public class Switch_case {

	public static void main(String[] args) {
		int e=200;
		switch(2){
			case 1:
				System.out.println("Case 1 is executed" +e);
				break;
			case 2:
				System.out.println("Case 2 is executed" +e);
				break;
			case 3:
				System.out.println("Case 3 is executed" +e);
				break;
				
				default:
					System.out.println("Default is executed" +e);
		}

	}

}
